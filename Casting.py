# Casting means that taking one particular type of data and converting it to other particular type of data
# For example

type1 = float(123)
print (type1)
# In above statement, float() is converting the data given in parenthesis to float

type2 = int(3.5)
print (type2)
# In above statement, int() is converting the data given in parenthesis to integer

type3 = complex (123)
print (type3)
# In above statement, complex () is converting the data given in parenthesis to a complex number

abc = str(12345)
print (abc)
# In this statement, str() is converting the data given in parenthesis to a string